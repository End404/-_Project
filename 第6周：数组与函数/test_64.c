#include <stdio.h>

int main()
{
//	二维数组
	int a[3][5];
	
//	二维数组的遍历
	for (i=0; i<3; i++ ) {
		for ( j=0; j<5; j++) {
			a[i][j] = i*j;
		}
	}
	
//	二维数组的初始化
	int a[][5] = {
	{0,1,2,3,4},
	{2,3,4,5,6},
	};
	
	return 0;
}


